const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const subCategoryValidation = require('../../validations/sub-category.validation');
const subCategoryController = require('../../controllers/sub-category.controller');

const router = express.Router();

router
  .route('/')
  .post(
    auth('sub-category.create'),
    validate(subCategoryValidation.createSubCategory),
    subCategoryController.createSubCategory
  )
  .get(validate(subCategoryValidation.getSubCategories), subCategoryController.getSubCategories);

router
  .route('/most-subscribed')
  .get(validate(subCategoryValidation.getSubCategories), subCategoryController.getMostSubscribedSubCategories);

router
  .route('/:subCategoryId')
  .get(auth('sub-category.get'), validate(subCategoryValidation.getSubCategory), subCategoryController.getSubCategory)
  .patch(
    auth('sub-category.update'),
    validate(subCategoryValidation.updateSubCategory),
    subCategoryController.updateSubCategory
  )
  .delete(
    auth('sub-category.delete'),
    validate(subCategoryValidation.deleteSubCategory),
    subCategoryController.deleteSubCategory
  );

module.exports = router;

